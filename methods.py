def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(a,b):
    soma = a+b
    return soma

def subtracao(a,b):
    subtracao = a-b
    return subtracao

def multiplicacao(a,b):
    multiplicacao = a*b
    return multiplicacao

def divisao(a,b):
    divisao = a/b
    return divisao
